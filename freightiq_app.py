def parse_terminal_rate(formula, cbm, weight_kg):
    if not formula or str(formula).strip() in ['nan', '', 'NaN']:
        return 0.0
    f = str(formula).upper().strip()
    weight_lb = weight_kg * 2.20462
    cwt = weight_lb / 100
    weight_ton = weight_lb / 2000

    calcs = []

    # $X/CWT (sum multiple if PLUS)
    cwt_rates = re.findall(r'\$([0-9.]+)/CWT', f)
    if cwt_rates:
        total = sum(float(x) for x in cwt_rates)
        calcs.append(('CWT', total * cwt))

    # $X/LB
    lb_match = re.search(r'\$([0-9.]+)/LB', f)
    if lb_match:
        calcs.append(('LB', float(lb_match.group(1)) * weight_lb))

    # 0.0X/LB (no dollar sign, e.g. 0.04/LB)
    lb2 = re.search(r'([0-9.]+)/LB', f)
    if lb2 and '$' not in f[max(0,f.find('/LB')-10):f.find('/LB')]:
        calcs.append(('LB_nodollar', float(lb2.group(1)) * weight_lb))

    # $X/CBM
    cbm_match = re.search(r'\$([0-9.]+)/CBM', f)
    if cbm_match:
        calcs.append(('CBM', float(cbm_match.group(1)) * cbm))

    # $X PER CBM/800 LBS (billing = max(cbm, lbs/800))
    cbm800 = re.search(r'\$([0-9.]+)\s*PER\s*CBM\s*/?\s*800\s*LBS?', f)
    if cbm800:
        rate = float(cbm800.group(1))
        billing = max(cbm, weight_lb / 800)
        calcs.append(('CBM800', rate * billing))

    # $X PER TON
    ton_match = re.search(r'\$([0-9.]+)\s*PER\s*TON', f)
    if ton_match:
        calcs.append(('TON', float(ton_match.group(1)) * weight_ton))

    # $X/CBM OR 1000LBS (SEA/PDX style: $7.50/CBM + $85 forklift)
    cbm_or_1000 = re.search(r'\$([0-9.]+)/CBM\s+OR\s+1000\s*LBS', f)
    if cbm_or_1000:
        rate = float(cbm_or_1000.group(1))
        billing = max(cbm, weight_lb / 1000)
        calcs.append(('CBM_1000', rate * billing))
        # Add forklift fee if present
        forklift = re.search(r'\$([0-9.]+)\s*FORKLIFT', f)
        if forklift:
            calcs[-1] = ('CBM_1000', calcs[-1][1] + float(forklift.group(1)))

    # LUMP SUM (WCT)
    lump = re.search(r'LUMP\s*SUM\s*\$([0-9.]+)', f)
    if lump:
        calcs.append(('LUMP', float(lump.group(1))))

    # $X.XX PER PALLET (NYK fallback: use $5.02/CWT logic)
    if 'PER PALLET' in f and not cwt_rates and not cbm_match:
        # Extract $X.XX/CWT from NYK style multi-line
        cwtny = re.search(r'\$([0-9.]+)/CWT\s*LOOSE', f)
        if cwtny:
            calcs.append(('CWT_NY', float(cwtny.group(1)) * cwt))

    if not calcs:
        return 0.0

    return max(v for _, v in calcs)

def parse_fsc(fsc_val, cbm, weight_kg):
    if not fsc_val or str(fsc_val).strip() in ['nan', '', 'NaN']:
        return 0.0
    weight_lb = weight_kg * 2.20462
    cwt = weight_lb / 100
    f = str(fsc_val).upper().strip()

    # Simple number
    try:
        return float(re.sub(r'[^0-9.]','', f)) if re.match(r'^[\d.]+$', f.strip()) else 0.0
    except:
        pass

    # $X/CWT
    m = re.search(r'\$?([0-9.]+)/CWT', f)
    if m:
        val = float(m.group(1)) * cwt
        # Check min/max
        min_m = re.search(r'MIN\s*\$?([0-9.]+)', f)
        max_m = re.search(r'MAX\s*\$?([0-9.]+)', f)
        if min_m: val = max(val, float(min_m.group(1)))
        if max_m: val = min(val, float(max_m.group(1)))
        return val

    # $X PER CBM
    m = re.search(r'\$([0-9.]+)\s*PER\s*CBM', f)
    if m:
        val = float(m.group(1)) * cbm
        min_m = re.search(r'MIN\s*\$?([0-9.]+)', f)
        if min_m: val = max(val, float(min_m.group(1)))
        return val

    # $X PER 1000 LBS
    m = re.search(r'\$([0-9.]+)\s*PER\s*1000\s*LBS', f)
    if m:
        val = float(m.group(1)) * (weight_lb / 1000)
        min_m = re.search(r'MIN\s*\$?([0-9.]+)', f)
        if min_m: val = max(val, float(min_m.group(1)))
        return val

    # $X PER CBM OR 800 LBS
    m = re.search(r'\$([0-9.]+)\s*PER\s*CBM\s*OR\s*800', f)
    if m:
        billing = max(cbm, weight_lb/800)
        val = float(m.group(1)) * billing
        min_m = re.search(r'MIN\s*\$?([0-9.]+)', f)
        if min_m: val = max(val, float(min_m.group(1)))
        return val

    # Just a number embedded
    nums = re.findall(r'\$([0-9.]+)', f)
    if nums:
        return float(nums[0])
    return 0.0

def parse_admin(admin_val, cbm, weight_kg):
    if not admin_val or str(admin_val).strip() in ['nan', '', 'NaN', ' ']:
        return 0.0
    weight_lb = weight_kg * 2.20462
    cwt = weight_lb / 100
    f = str(admin_val).upper().strip()

    # Simple number
    try:
        v = float(str(admin_val).strip())
        return v
    except:
        pass

    # Sum all dollar amounts mentioned (e.g. TSC FEE $40 + SECURITY FEE $6.50)
    all_dollars = re.findall(r'\$([0-9.]+)', f)
    if all_dollars:
        # Don't sum if it's a rate formula
        if '/CWT' in f or '/CBM' in f or '/LB' in f:
            m = re.search(r'\$([0-9.]+)/CWT', f)
            if m:
                val = float(m.group(1)) * cwt
                min_m = re.search(r'MIN\s*\$?([0-9.]+)', f)
                if min_m: val = max(val, float(min_m.group(1)))
                return val
        else:
            # Sum all fixed amounts
            return sum(float(x) for x in all_dollars)
    return 0.0

def parse_fixed(val):
    """Parse a simple numeric value or return 0."""
    if val is None or str(val).strip() in ['nan', '', 'NaN', ' ']:
        return 0.0
    try:
        return float(str(val).strip())
    except:
        nums = re.findall(r'([0-9.]+)', str(val))
        return float(nums[0]) if nums else 0.0

