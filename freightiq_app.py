def load_data():
    destinations = pd.read_excel('IPI Destinations (1).xlsx')
    rates_raw = pd.read_excel('IPI Facility Rates.xlsx', header=0)

    # Parse destinations
    dest_clean = []
    for _, r in destinations.iterrows():
        code = str(r.get('CODE', '')).strip()
        if not code or code == 'nan':
            continue
        dest_clean.append({
            'code': code,
            'destination': str(r.get('DESTINATION', '')).strip(),
            'name': str(r.get('NAME', '')).strip(),
            'address': str(r.get('ADDRESS', '')).strip(),
            'city_state': str(r.get('CITY-STATE', '')).strip(),
            'zip': str(r.get('ZIP', '')).strip(),
            'contact': str(r.get('CONTACT', '')).strip(),
            'telephone': str(r.get('TELEPHONE', '')).strip(),
            'website': str(r.get('WEBSITE', '')).strip(),
            'email': str(r.get('EMAIL', '')).strip(),
            'hours': str(r.get('HOURS', '')).strip(),
        })

    # Parse facility rates
    rates_clean = []
    for i, r in rates_raw.iterrows():
        code = str(r.iloc[0]).strip()
        if not code or code in ['nan', 'CODE']:
            continue
        if len(code) > 5:
            continue
        rates_clean.append({
            'code': code,
            'company': str(r.iloc[1]).strip(),
            'terminal_formula': str(r.iloc[2]).strip(),
            'min_charge': r.iloc[3],
            'max_charge': r.iloc[4],
            'fsc': r.iloc[5],
            'admin': r.iloc[6],
            'fac': r.iloc[7],
            'doc_fee': r.iloc[9],
        })

    return pd.DataFrame(dest_clean), pd.DataFrame(rates_clean)

